package loops;

public class tentoone {
    public static void main(String[] args) {
        for (int i=10;i>=1;i--){
            System.out.print(i+" ");
        }
        System.out.println();

        int j=10;
        while (j>=1){
            System.out.print(j+" ");
            j--;
        }

    }
}
